const (
	INDEX_PAGE_SIZE_5  = "5"
	INDEX_PAGE_SIZE_10 = "10"
	INDEX_PAGE_SIZE_20 = "20"
	INDEX_PAGE_SIZE_30 = "30"
)

func ConvertPageSize2Int(pageSize string) (int, error) {
	if pageSize == INDEX_PAGE_SIZE_5 {
		return 5, nil
	} else if pageSize == INDEX_PAGE_SIZE_10 {
		return 10, nil
	} else if pageSize == INDEX_PAGE_SIZE_20 {
		return 20, nil
	} else if pageSize == INDEX_PAGE_SIZE_30 {
		return 30, nil
	}
	fmt.Println("ConvertPageSize2Int: Wrong page size,should be 5 10 20 30 in string")
	return -1, errors.New("Wrong page size,should be 5 10 20 30 in string")
}
